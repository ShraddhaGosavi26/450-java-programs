public class swap {
    public static void main(String[] args) {
        int a=10;
        int b=5;
        int temp;
        System.out.println("Before Swapping value of a : "+a+" value of b: "+b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("After Swapping value of a : "+a+" value of b: "+b);
    }
}
